package Retrieval.core.utils;

public class BIMTermProb {
    private Character term;
    private double pi;
    private double ri;

    public BIMTermProb(){}

    public Character getTerm() { return term; }

    public void setTerm(Character term) { this.term = term; }

    public double getPi() { return pi; }

    public void setPi(double pi) { this.pi = pi; }

    public double getRi() { return ri; }

    public void setRi(double ri) {this.ri = ri; }
}
